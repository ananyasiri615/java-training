package day_four_java;

public class StringExample {

	public static void main(String[] args) {			
		String s1 = "Meera";
		System.out.println(s1);
			
		s1 = "Arjun";
		System.out.println(s1);
			
		s1 = s1.concat(" Kumar");
		System.out.println(s1);
			
		s1 = s1.concat(" Singh");
		System.out.println(s1);

		s1 = s1.toUpperCase();
		System.out.println(s1);
	}

}

